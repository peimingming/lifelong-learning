#include <iostream>
using namespace std;

int main(int argc, const char * argv[]) {
    int a;
    // cin使用右移动运算符>>，将输入的数值赋值给a
    cin >> a;
    // cout使用左移运算符<<，输出a的值
    cout << "a = " << a << endl; // endl 表示换行
    
    return 0;
}
